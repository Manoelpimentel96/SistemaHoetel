    private void preencherTabela(List<Funcionario> lista) {
        DefaultTableModel model = (DefaultTableModel) tableDadosFuncionarios.getModel();
        model.setRowCount(0);
        for (Funcionario f : lista) {
            model.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCpf(),
                f.getCargo(),
                f.getStatus(),
                f.getSalario(),
                f.getLogin(),
                "********",
                f.getTelefone(),
                f.getEmail(),
                f.getDataAdmissao()
            });
        }
    }

