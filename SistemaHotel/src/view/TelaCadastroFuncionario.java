    private boolean validarCamposObrigatorios() {
        if (campoNome.getText().trim().isEmpty()
                || campoCPF.getText().trim().equals("   .   .   -  ")
                || campoLogin.getText().trim().isEmpty()
                || campoPassword.getPassword().length == 0
                || ComboCargo.getSelectedIndex() == 0
                || comboStatus.getSelectedIndex() == 0
                || jDateAdmissao.getDate() == null
                || campoSalario.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this,
                    "Preencha todos os campos obrigatórios!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

    //usado no atualizar
    private boolean validarCamposObrigatoriosAtualizacao() {
        if (campoNome.getText().trim().isEmpty()
                || campoCPF.getText().trim().isEmpty()
                || campoLogin.getText().trim().isEmpty()
                || campoEmail.getText().trim().isEmpty()
                || campoTelefone.getText().trim().isEmpty()
                || ComboCargo.getSelectedIndex() == 0
                || comboStatus.getSelectedIndex() == 0
                || jDateAdmissao.getDate() == null) {
            JOptionPane.showMessageDialog(this,
                    "Preencha todos os campos obrigatórios (exceto senha).");
            return false;
        }
        return true;
    }

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
            if (jRadioListarTodosFuncionarios.isSelected()) {
                preencherTabela(controller.listarTodos());
            } else {
                String cpf = campoConsultarCpf.getText();
                if (cpf.trim().equals("   .   .   -  ")) {
                    JOptionPane.showMessageDialog(this,
                            "Informe um CPF para consultar!",
                            "Aviso", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                Funcionario f = controller.buscarPorCpf(cpf);
                if (f != null) {
                    preencherTabela(List.of(f));
                } else {
                    JOptionPane.showMessageDialog(this, "Funcionário não encontrado!");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro na consulta: " + e.getMessage());
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int linha = tableDadosFuncionarios.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário na tabela para editar!");
            return;
        }

        try {
            campoNome.setText(tableDadosFuncionarios.getValueAt(linha, 1).toString());
            campoCPF.setText(tableDadosFuncionarios.getValueAt(linha, 2).toString());
            ComboCargo.setSelectedItem(tableDadosFuncionarios.getValueAt(linha, 3).toString());
            comboStatus.setSelectedItem(tableDadosFuncionarios.getValueAt(linha, 4).toString());
            campoSalario.setText(tableDadosFuncionarios.getValueAt(linha, 5).toString());
            campoLogin.setText(tableDadosFuncionarios.getValueAt(linha, 6).toString());
            campoTelefone.setText(tableDadosFuncionarios.getValueAt(linha, 8).toString());
            campoEmail.setText(tableDadosFuncionarios.getValueAt(linha, 9).toString());

            // Conversão segura da data
            String dataStr = tableDadosFuncionarios.getValueAt(linha, 10).toString();
            if (dataStr != null && !dataStr.isEmpty()) {
                LocalDate localDate = LocalDate.parse(dataStr);
                jDateAdmissao.setDate(java.sql.Date.valueOf(localDate));
            } else {
                jDateAdmissao.setDate(null);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher os campos: " + e.getMessage());
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:                                         

        int linha = tableDadosFuncionarios.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário para excluir!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Deseja realmente excluir este funcionário?\n"
                + "Se ele tiver pagamentos, será colocado como PAUSADO.",
                "Confirmação", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {

            int id = (int) tableDadosFuncionarios.getValueAt(linha, 0);

            controller.excluir(id);

            JOptionPane.showMessageDialog(this,
                    "Operação realizada com sucesso!\n"
                    + "Se existiam pagamentos, o funcionário foi PAUSADO.");

            preencherTabela(controller.listarTodos());
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (!validarCamposObrigatorios()) {
            return;
        }

        try {
            String cpfSemMascara = campoCPF.getText().replaceAll("\\D", "");

            // Verifica duplicidade de CPF
            if (controller.existePorCpf(cpfSemMascara)) {
                JOptionPane.showMessageDialog(this,
                        "Já existe um funcionário cadastrado com este CPF!",
                        "Duplicidade de CPF", JOptionPane.WARNING_MESSAGE);
                return;
            }
            Funcionario f = new Funcionario();
            f.setNome(campoNome.getText());
            //f.setCpf(campoCPF.getText());
            // Remove máscara e define o CPF sem pontos e traço
            f.setCpf(campoCPF.getText().replaceAll("\\D", ""));

            f.setCargo(ComboCargo.getSelectedItem().toString());
            f.setEmail(campoEmail.getText());
            f.setTelefone(campoTelefone.getText());
            f.setLogin(campoLogin.getText());
            f.setSenha(Criptografia.gerarHash(new String(campoPassword.getPassword())));
            f.setStatus(comboStatus.getSelectedItem().toString().toUpperCase());
            f.setRoleId(Funcionario.obterRoleIdPorCargo(ComboCargo.getSelectedItem().toString()));
            f.setDataAdmissao(jDateAdmissao.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
            f.setSalario(CampoMoeda.getValor(campoSalario));

            controller.inserir(f);

            JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso!");
            limparCampos();
            preencherTabela(controller.listarTodos());// Exibe o funcionario na tabela
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar funcionário: " + e.getMessage());
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarDadosActionPerformed
        // TODO add your handling code here:
        try {
            if (!validarCamposObrigatoriosAtualizacao()) {
                return;
            }

            int linha = tableDadosFuncionarios.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um funcionário para atualizar.");
                return;
            }

            Funcionario f = new Funcionario();
            f.setId(Integer.parseInt(tableDadosFuncionarios.getValueAt(linha, 0).toString()));
            f.setNome(campoNome.getText().trim());
            f.setCpf(campoCPF.getText().replaceAll("\\D", "")); // remove máscara
            f.setCargo(ComboCargo.getSelectedItem().toString());
            f.setEmail(campoEmail.getText().trim());
            f.setTelefone(campoTelefone.getText().trim());
            f.setLogin(campoLogin.getText().trim());
            f.setStatus(comboStatus.getSelectedItem().toString());
            f.setRoleId(Funcionario.obterRoleIdPorCargo(ComboCargo.getSelectedItem().toString()));
            f.setDataAdmissao(jDateAdmissao.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
            f.setSalario(CampoMoeda.getValor(campoSalario));

            // Atualiza a senha somente se o campo foi preenchido
            String novaSenha = new String(campoPassword.getPassword()).trim();
            if (!novaSenha.isEmpty()) {
                f.setSenha(Criptografia.gerarHash(novaSenha));
            } else {
                // Mantém a senha atual
                Funcionario existente = controller.buscarPorId(f.getId());
                if (existente != null) {
                    f.setSenha(existente.getSenha());
                }
            }

            // Chama o controller para atualizar
            controller.atualizar(f);

            JOptionPane.showMessageDialog(this, "Dados atualizados com sucesso!");
            preencherTabela(controller.listarTodos());

            // Atualiza usuário logado (sincroniza TelaPrincipal)
            Funcionario logado = util.UsuarioLogado.getUsuario();
            if (logado != null && logado.getId() == f.getId()) {
                util.UsuarioLogado.setUsuario(f);
            }

            // Limpar os campos depois da ação
            limparCampos();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao atualizar funcionário: " + e.getMessage());
        }

    }//GEN-LAST:event_btnAtualizarDadosActionPerformed

